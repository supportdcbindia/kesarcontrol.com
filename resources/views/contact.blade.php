<script>
document.addEventListener('DOMContentLoaded', function() {
    const select = document.getElementById('country');
    if (!select) return;

    let buffer = '';
    let lastTime = 0;
    const RESET_MS = 800;

    // Normalize: remove diacritics, lowercase, trim
    function normalizeText(str) {
        return String(str || '')
            .normalize('NFD')
            .replace(/[\u0300-\u036f]/g, '') // strip accents
            .toLowerCase()
            .trim();
    }

    // If using the "nice-select" plugin, update its visible UI container (if present)
    function updateNiceSelectUI(sel) {
        const next = sel.nextElementSibling;
        if (next && next.classList && next.classList.contains('nice-select')) {
            const current = next.querySelector('.current');
            if (current) current.textContent = sel.options[sel.selectedIndex]?.text || '';

            // Update selected class on list items if present
            const listItems = next.querySelectorAll('ul.list li');
            if (listItems.length) {
                listItems.forEach(li => li.classList.remove('selected'));
                const val = sel.value;
                const matchLi = Array.from(listItems).find(li =>
                    li.getAttribute('data-value') === val ||
                    normalizeText(li.textContent) === normalizeText(sel.options[sel.selectedIndex]?.text)
                );
                if (matchLi) matchLi.classList.add('selected');
            }
        }
    }

    document.addEventListener('keydown', function(e) {
        // ignore modifier combos
        if (e.ctrlKey || e.metaKey || e.altKey) return;

        // don't run while user is typing in an input/textarea/contenteditable or another select
        const active = document.activeElement;
        if (active && (active.tagName === 'INPUT' || active.tagName === 'TEXTAREA' || active
                .isContentEditable)) return;
        if (active && active.tagName === 'SELECT' && active !== select) return;

        const key = e.key;
        if (!key || key.length !== 1) return; // only single printable characters

        const now = Date.now();
        if (now - lastTime > RESET_MS) buffer = '';
        lastTime = now;
        buffer += key;

        const search = normalizeText(buffer);
        let foundIndex = -1;

        // If single-char search, start after current selection so repeated presses cycle through matches
        if (buffer.length === 1) {
            const start = (select.selectedIndex >= 0 ? select.selectedIndex + 1 : 0);
            for (let offset = 0; offset < select.options.length; offset++) {
                const i = (start + offset) % select.options.length;
                if (normalizeText(select.options[i].text).startsWith(search)) {
                    foundIndex = i;
                    break;
                }
            }
        } else {
            // multi-char search: match from top
            for (let i = 0; i < select.options.length; i++) {
                if (normalizeText(select.options[i].text).startsWith(search)) {
                    foundIndex = i;
                    break;
                }
            }
        }

        if (foundIndex >= 0) {
            select.selectedIndex = foundIndex;
            select.dispatchEvent(new Event('change', {
                bubbles: true
            }));
            updateNiceSelectUI(select);
            e.preventDefault();
            return;
        }

        // Fallback: if multi-letter buffer fails, try last single char to allow cycling by repeating a letter
        if (buffer.length > 1) {
            const lastChar = normalizeText(key);
            for (let offset = 0; offset < select.options.length; offset++) {
                const i = (select.selectedIndex + 1 + offset) % select.options.length;
                if (normalizeText(select.options[i].text).startsWith(lastChar)) {
                    select.selectedIndex = i;
                    select.dispatchEvent(new Event('change', {
                        bubbles: true
                    }));
                    updateNiceSelectUI(select);
                    e.preventDefault();
                    break;
                }
            }
        }
    });
});
